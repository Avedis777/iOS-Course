import UIKit

/*
 In Interface Builder I did the following setup:
 1) Connect 'Table View' dataSource outlet to 'Table View Controller' in Document Outline
 2) Select the cell in the table view, go to Attributes and set the Indentifier to 'colorCell'
 */

class TableViewController: UITableViewController {

    var model: Model! = nil

    override func viewDidLoad() {
        super.viewDidLoad()
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return model.colorNames.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "colorCell", for: indexPath)
        cell.textLabel?.text = model.colorNames[indexPath.row]
        return cell
    }

